// Adição
let n1 = 20;
let n2 = 10;
let soma = n1 + n2;
console.log(soma);

// Subtração
let sub = n1 - n2;
console.log(sub);

// Multiplicação
let mult = n1 * n2;
console.log(mult);

// Divisão
let div = n1 / n2;
console.log(div);

// Resto ou mod
let mod = n1 % n2;
console.log(mod);

// Incremento
console.log(n1++);
console.log(++n1);

// Decremento
console.log(n2--);
console.log(--n2);
